/**
 * Retourne la langue actuelle (détection navigateur + localStorage)
 */
export function getLang() {
    try {
        const stored = localStorage.getItem(STORAGE_KEY);
        if (stored && TRANSLATIONS[stored]) return stored;
    } catch {}
    const browserLang = (navigator.language || 'fr').slice(0, 2).toLowerCase();
    return TRANSLATIONS[browserLang] ? browserLang : 'fr';
}

/**
 * Définit la langue et met à jour l'interface
 */
export function setLang(lang) {
    if (!TRANSLATIONS[lang]) lang = 'fr';
    try {
        localStorage.setItem(STORAGE_KEY, lang);
    } catch {}
    translatePage();
    window.dispatchEvent(new CustomEvent('languagechange', { detail: { lang } }));
}

/**
 * Traduit une clé avec variables optionnelles
 * @param {string} key - Clé de traduction
 * @param {Object} vars - Variables à injecter (ex: {count: 5})
 * @returns {string} Texte traduit
 */
export function t(key, vars = {}) {
    const lang = getLang();
    let text = TRANSLATIONS[lang]?.[key];
    if (text === undefined) {
        text = TRANSLATIONS['fr']?.[key] || key;
    }
    if (vars) {
        Object.entries(vars).forEach(([k, v]) => {
            text = text.replace(`{${k}}`, v);
        });
    }
    return text;
}

/**
 * Traduit tous les éléments data-i18n du DOM
 */
export function translatePage() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        el.textContent = t(key);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        el.placeholder = t(key);
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        const key = el.getAttribute('data-i18n-title');
        el.title = t(key);
    });
    // Mettre à jour le html lang
    document.documentElement.lang = getLang();
}
